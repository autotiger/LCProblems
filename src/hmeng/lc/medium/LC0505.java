    private int rowLen;
    private int colLen;

    private static class StartPoint {
        int[] start;
        int lenSoFar;
        StartPoint(int[] start, int lenSoFar) {
            this.start = start;
            this.lenSoFar = lenSoFar;
        }
        @Override
        public String toString() {
            return new StringBuilder().append("(").append(start[0]).append(",").append(start[1]).append(")")
                    .append("-").append("len: ").append(lenSoFar).toString();
        }
    }
    
    public int shortestDistance(int[][] maze, int[] start, int[] destination) {
        if (Arrays.equals(start, destination)) return 0;
        rowLen = maze.length;
        colLen = maze[0].length;
        int[][] m = new int[rowLen][colLen];
        for (int i = 0; i < m.length; i++) {
            for (int j = 0; j < m[0].length; j++) m[i][j] = Integer.MAX_VALUE;
        }
        PriorityQueue<StartPoint> queue = new PriorityQueue<>((l, r) -> l.lenSoFar - r.lenSoFar);
        queue.offer(new StartPoint(start, 0));
        while (!queue.isEmpty()) {
            StartPoint currStart = queue.poll();
            if (m[currStart.start[0]][currStart.start[1]] <= currStart.lenSoFar) continue;
            m[currStart.start[0]][currStart.start[1]] = currStart.lenSoFar;
            for (int i = 0; i < DIR.length; i++) {
                queue.offer(roll(currStart, i, maze));
            }
        }

        return m[destination[0]][destination[1]] < Integer.MAX_VALUE ? m[destination[0]][destination[1]] : -1;
    }
    
    private StartPoint roll(StartPoint s, int d, int[][] maze) {
        int row = s.start[0], col = s.start[1];
        int len = 0;
        while (canRoll(s.start, row+DIR[d][0], col+DIR[d][1], maze)) {
            row += DIR[d][0];
            col += DIR[d][1];
            len++;
        }
        
        return new StartPoint(new int[]{row, col}, s.lenSoFar+len);
    }
    
    private boolean canRoll(int[] start, int row, int col, int[][] maze) {
        if (row < 0 || row >= rowLen || col < 0 || col >= colLen || maze[row][col] == 1) return false;
        return true;
    }

